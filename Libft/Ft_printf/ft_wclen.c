#include "libftprintf.h"

int		ft_wclen(wchar_t wc)
{
	int ret;

	ret = 0;
	if (wc <= 127)
		ret++;
	else if (wc <= 2047)
		ret += 2;
	else if (wc <= 65535)
		ret += 3;
	else if (wc <= 1114111)
		ret += 4;
	return (ret);
}
