#include "libftprintf.h"

int		ft_wstrlen(wchar_t *s)
{
	int i;
	int j;

	j = 0;
	i = 0;
	if (s)
	{
		while (s[i])
		{
			if (s[i] <= 127)
				j++;
			else if (s[i] <= 2047)
				j += 2;
			else if (s[i] <= 65535)
				j += 3;
			else if (s[i] <= 1114111)
				j += 4;
			i++;
		}
	}
	return (j);
}
