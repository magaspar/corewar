#include "libft.h"

void	ft_put_uint(unsigned int n)
{
	if (n >= 10)
	{
		ft_put_uint(n / 10);
		ft_putchar(n % 10 + '0');
	}
	if (n < 10)
		ft_putchar(n + '0');
}
