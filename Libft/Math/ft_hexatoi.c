#include "libft.h"

int		ft_hexatoi(const char *s)
{
	int nb;

	nb = 0;
	while (*s == 32)
		s++;
	if (*s == '0' && s++)
		if (*s++ != 'x')
			return (0);
	while ((*s >= '0' && *s <= '9') || (*s >= 'A' && *s <= 'F')
		|| (*s >= 'a' && *s <= 'f'))
	{
		if (*s >= '0' && *s <= '9')
			nb = nb * 16 + (*s++ - '0');
		else if (*s >= 'a' && *s <= 'f')
			nb = nb * 16 + (*s++ - 'W');
		else
			nb = nb * 16 + (*s++ - '7');
	}
	return (nb);
}
